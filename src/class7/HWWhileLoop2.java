package class7;

public class HWWhileLoop2 {
    public static void main(String[] args) {
        //Print numbers from 100 to 1
        int counter=100;
        while (counter>=1){
            System.out.print(counter+" ");
            counter--;
        }
        System.out.println();
        for (int i = 100; i >=1 ; i--) {
            System.out.print(i+" ");
        }
    }
}
