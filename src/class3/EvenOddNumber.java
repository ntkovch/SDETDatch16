package class3;

public class EvenOddNumber {
    public static void main(String[] args) {
        int num1 = 86;
        if (num1 % 2==0) {
            System.out.println("Number " + num1 + " is even");

        } else {
            System.out.println("Number " + num1 + " is odd");
        }
    }
}
